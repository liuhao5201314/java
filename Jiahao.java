//二边为数值型，则做加法运算
//一边为字符则做拼接运算
//运算顺序从左向右
public class Jiahao{
	public static void main(String[] args){
		System.out.println(100+98);//198
		System.out.println("100"+98);//10098
		System.out.println(100+3+"hello");//103hello
		System.out.println("hello"+100+3);//hello100+3=hello1003
	}
}

